            <p>These files are free and always will be. If you find this useful and would like to say thank you, please consider making a donation to the <a href="https://pkdcharity.charitycheckout.co.uk/profile">Polycystic Kidney Disease Charity UK</a>.</p>
            <p>My late father had autosomal dominant polycystic kidney disease (ADPKD), and died of kidney failure in January 1998. I have ADPDK, my sister and brother both have ADPKD; there is a 50/50 chance that my children have ADPKD.</p>
            <p>The <a href="https://pkdcharity.charitycheckout.co.uk/profile">PKD Charity</a> was established in December 2000 by patients, professionals and members of families affected by PKD, they have provided tremendous support to members of my family and work to fund research into determining the causes of polycystic kidney disease and into discovering treatments and a cure for PKD.</p>

